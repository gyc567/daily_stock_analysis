def expand_keyword(keyword: str) -> List[str]:
    """单个关键词 -> 同义词组。

    命中规范词或某个同义词组内的词，返回整组；否则返回 ``[keyword]``。
    """
    if keyword in SYNONYMS:
        return SYNONYMS[keyword]
    for synonyms in SYNONYMS.values():
        if keyword in synonyms:
            return synonyms
    return [keyword]


def expand_keywords(keywords: List[str]) -> List[List[str]]:
    """关键词列表 -> 同义词组列表（保持顺序、不去重原始关键词）。"""
    return [expand_keyword(k) for k in keywords]
